console.log('Четные числа:')
for (let number: number = 0; number <= 100; number++) {
    if (number % 2 === 0) {
        console.log(number)
    }
}
console.log('Нечетные числа:')
for (let number: number = 0; number <= 100; number++) {
    if (number % 2 !== 0) {
        console.log(number)
    }
}

